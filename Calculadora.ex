defmodule Calculadora do
  def suma(n1, n2) do
    IO.puts "la suma entre #{n1} y #{n2} es : "
    n1 + n2
  end

  def resta(n1, n2) do
    IO.puts "la resta entre #{n1} y #{n2} es : "
    n1 - n2
  end

  def multiplicacion(n1, n2) do
    IO.puts "la multiplicacion entre #{n1} y #{n2} es : "
    n1 * n2
  end

  def dividir(n1, n2) do
    IO.puts "la multiplicacion entre #{n1} y #{n2} es : "
    n1 / n2
  end

end
